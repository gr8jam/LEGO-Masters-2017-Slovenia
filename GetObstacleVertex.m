function [ obst ] = GetObstacleVertex( x,y )
N = length(x);
obst = zeros(8 * N,4);
for i = 1:N
    j = i - 1;
    s = [x(i),y(i)];

    v = zeros(2,8); % x,y

    R = 54;
    % 1
    v(1,1) = s(1) - sin(22.5 * pi / 180) * R;
    v(2,1) = s(2) + cos(22.5 * pi / 180) * R;
    % 2
    v(1,2) = s(1) + sin(22.5 * pi / 180) * R;
    v(2,2) = s(2) + cos(22.5 * pi / 180) * R;
    % 3
    v(1,3) = s(1) + cos(22.5 * pi / 180) * R;
    v(2,3) = s(2) + sin(22.5 * pi / 180) * R;
    % 4
    v(1,4) = s(1) + cos(22.5 * pi / 180) * R;
    v(2,4) = s(2) - sin(22.5 * pi / 180) * R;
    % 5
    v(1,5) = s(1) + sin(22.5 * pi / 180) * R;
    v(2,5) = s(2) - cos(22.5 * pi / 180) * R;
    % 6
    v(1,6) = s(1) - sin(22.5 * pi / 180) * R;
    v(2,6) = s(2) - cos(22.5 * pi / 180) * R;
    % 7
    v(1,7) = s(1) - cos(22.5 * pi / 180) * R;
    v(2,7) = s(2) - sin(22.5 * pi / 180) * R;
    % 8
    v(1,8) = s(1) - cos(22.5 * pi / 180) * R;
    v(2,8) = s(2) + sin(22.5 * pi / 180) * R;

    

    obst(1 + j * 8,:) = [v(1,1) v(2,1) v(1,2) v(2,2)];
    obst(2 + j * 8,:) = [v(1,2) v(2,2) v(1,3) v(2,3)];
    obst(3 + j * 8,:) = [v(1,3) v(2,3) v(1,4) v(2,4)];
    obst(4 + j * 8,:) = [v(1,4) v(2,4) v(1,5) v(2,5)];
    obst(5 + j * 8,:) = [v(1,5) v(2,5) v(1,6) v(2,6)];
    obst(6 + j * 8,:) = [v(1,6) v(2,6) v(1,7) v(2,7)];
    obst(7 + j * 8,:) = [v(1,7) v(2,7) v(1,8) v(2,8)];
    obst(8 + j * 8,:) = [v(1,8) v(2,8) v(1,1) v(2,1)];
end


end

